    //saveStock saves changes to stocks to a file
    public void saveStock(String updStock){
        try {
            Formatter oFile = new Formatter("stock.txt");
            oFile.format("%s",updStock); // write information about the project to file
            oFile.close(); // close file after finish writing   
	} catch (IOException e) {
            System.out.println("**An error occurred writing to stock.txt file**");
            e.printStackTrace();
	}	
    }  
    
    // read stock from txt file to a textArea on form
    public void readStock(){
        String data = "";
        try {
            File myObj = new File("stock.txt");
            Scanner myReader = new Scanner(myObj);
            while (myReader.hasNextLine()) {
                data += myReader.nextLine() +"\n";
                
            }
            
            // save stock name and quantity in an array(stockList)
            String tmpData = data;
            tmpData = tmpData.replace("\n", ", ");
            String[] tokens = tmpData.split(", ");
            
            stockList = new ArrayList<>();
            for(int i = 0; i< tokens.length; i++){
                    stockList.add(tokens[i]);  
            }
            
            jTextArea1.setText(data);
            myReader.close();
            
        } catch (FileNotFoundException e) {
            System.out.println("An error occurred.");
            e.printStackTrace();
        }
    }
    
