    private void buyBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buyBtnActionPerformed
        // purchase items selected if there is enough money
   
        Double money = 0.0;
        DecimalFormat numberFormat = new DecimalFormat("#.00"); // two decial place format
        //String strMoney = moneyTxtField.getText();
        if(moneyTxtField.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "There\'s no money");
            return;
        }
        
        try{
            money = Double.parseDouble(moneyTxtField.getText());
        } catch(Exception e){
            JOptionPane.showMessageDialog(this, " wrong money");
            return;
        }
        
        Integer stockInt = 0;
        boolean anyIsSelected = false; // check if any item is selected 
        // deduce from money inserted according to selected items
        for(javax.swing.JCheckBox item: prices.keySet()){
            if(item.isSelected()){
                // code bellow deducts from stock before buying 
               for(int i = 0; i < stockList.size(); i+=2)
                   if(item.getText().contains(stockList.get(i))){
                       stockInt = Integer.parseInt(stockList.get(i+1));
                       if(stockInt == 0){
                           JOptionPane.showMessageDialog(this, " Out of stock for " + item.getText());
                           item.setSelected(false);
                          return;
                         }
                       stockInt--; // decrease stock of item
                       stockList.set(i+1, stockInt.toString());
                   }
               
               
                money -= prices.get(item);
                
                anyIsSelected = true;
            }
        }
        
        // first check that user has selected any item or not
        if(!anyIsSelected){
            JOptionPane.showMessageDialog(this, "No item is selected");
            return;
        }
        // check to see if user's money is enough or not
        if(money < 0){ // money is not enough
            Double owed = Math.abs(money);
             JOptionPane.showMessageDialog(this, "Sorry! your money is not enough ! short of: " + numberFormat.format(owed));
        } else{ // purchase is complete
               
              // update stock available
            String updStock ="";
            for(int i = 0; i <stockList.size();i+=2) {
                updStock += stockList.get(i) +", "+ stockList.get(i+1) +"\n";
            }
            // Save stock list to file
            saveStock(updStock);
            jTextArea1.setText(updStock);
             // Output change
            //Double.toString(money)
            changeTxtField.setText(numberFormat.format(money));
            String message = "========ITEMS BOUGHT============= \n";
            
            for(javax.swing.JCheckBox item: prices.keySet()){
                if(item.isSelected()){
                    message += item.getText()+"\n";
            }
        }
            JOptionPane.showMessageDialog(this, "Your Purchase has been completed\n" + message);
        }
        
        
    }//GEN-LAST:event_buyBtnActionPerformed

